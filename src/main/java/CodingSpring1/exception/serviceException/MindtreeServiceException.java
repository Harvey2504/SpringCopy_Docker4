package CodingSpring1.exception.serviceException;

public class MindtreeServiceException extends Exception {
	public MindtreeServiceException() {
		super();
	}

	public MindtreeServiceException(Throwable arg0) {
		super(arg0);
	}

	public MindtreeServiceException(String arg0) {
		super(arg0);
	}

	public MindtreeServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}

package CodingSpring1.exception;

public class MindtreeCustomException extends Exception {

	public MindtreeCustomException() {
		super();
	}

	public MindtreeCustomException(Throwable arg0) {
		super(arg0);
	}

	public MindtreeCustomException(String arg0) {
		super(arg0);
	}

	public MindtreeCustomException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
